    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        Locale localeBR = new Locale("pt","BR");
        NumberFormat numberFormat = NumberFormat.getNumberInstance(localeBR);
        NumberFormat dinheiro = NumberFormat.getCurrencyInstance(localeBR);

        String nome = txtnome.getText();
        Double salario = Double.valueOf(txtsalario.getText());
        Double reajuste = Double.valueOf(txtreajuste.getText());
        salario = salario + (salario * reajuste /100);
        String mensagem = nome + ", seu novo salário será: " + dinheiro.format(salario);
        JOptionPane.showMessageDialog(null, mensagem);
        lblnovosalario.setText(dinheiro.format(salario).toString());
        
    }//GEN-LAST:event_jButton1ActionPerformed

